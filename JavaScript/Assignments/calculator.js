function add(val1, val2){
    return val1 + val2;
}

function sub(val1, val2){
    return val1 - val2;
}

function mul(val1, val2){
    return val1 * val2;
}

function div(val1, val2){
    return val1 / val2;
}


console.log("Addition: " , add(5, 8));
console.log("Subtraction: " , sub(55, 15));
console.log("Multiplication: " , mul(255, 5));
console.log("Division: " , div(90, 15));

